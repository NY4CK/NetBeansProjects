    private void btnExcluirConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirConsultaActionPerformed
        
    int selectedRow = tblTabela.getSelectedRow();
    
    // Verifica se alguma linha foi selecionada
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma consulta para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Remove a consulta selecionada da tabela
    DefaultTableModel model = (DefaultTableModel) tblTabela.getModel();
    model.removeRow(selectedRow);
    
    // Mostra uma mensagem de sucesso
    JOptionPane.showMessageDialog(this, "Consulta exclu√≠da com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnExcluirConsultaActionPerformed

    private void btnFinalizarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarConsultaActionPerformed
        
        int selectedRow = tblTabela.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma consulta para finalizar.", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    DetalheConsulta detalheConsulta = new DetalheConsulta();
    detalheConsulta.setConsultaSelecionada(selectedRow);
    detalheConsulta.setVisible(true);
    detalheConsulta.setLocationRelativeTo(this);
    }//GEN-LAST:event_btnFinalizarConsultaActionPerformed

